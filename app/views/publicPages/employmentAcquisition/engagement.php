            <div class="contentHeading col-lg-12">
                <h3>Capable of Gaining Intimate Product & User Knowledge.</h3>
            </div>
        </div>
        <div class="contentBodySection row">
            <div class="contentImg col-lg-12">
                <img class="img-circle img-responsive center-block" src="/assets/engagementContentIcons/1.png">
            </div>
            <div class="contentSection col-lg-6 col-lg-offset-3 col-md-6 col-md-offset-3 col-sm-8 col-sm-offset-2 col-md-6 col-md-offset-3 col-sm-8 col-sm-offset-2">
                <div class="articleWhatSection">
                    <h4>What is this trait?</h4>
                    <p>
                        Its two parts to this trait.
                        The first is being able to understand all the problems your product or service solves.
                        And the second is fully understanding the various groups likely to experience those problems.
                    </p>
                </div>
                <div class="articleBenefitSection">
                    <h4>Benefits to your business:</h4>
                    <p>
                        Having a marketing team that is knowledgeable about the product and customer yields a number of rewards to a business.
                        Empathetic marketers can develop better sales copy and more appropriate acquisition strategies.
                        While a more competent sales force can adequately answer product questions and prevent bottlenecks during the sales process.
                    </p>
                </div>
                <div class="articleSpotSection">
                    <h4>How to Spot it?</h4>
                    <p>
                        This is fairly easy trait to spot in prospective hires.
                        One give away is if a candidate comes to the interview with a solid understanding of your business model.
                        Not just the company history, but what you sell, who you sell it to, and the problems it solves for your customers.
                        Another easy spot is if a candidate actually fits your target customer profile.
                        This may be rare but is a great opportunity to bring a team member on board who can really be effective for your company.
                    </p>
                </div>
                <div class="articleFoolSection">
                    <h4>Don't be fooled!</h4>
                    <p>
                        Nowadays, interviewees are smarter.
                        There are more resources available to help them 'ace the interview'.
                        What you don’t want to be fooled by is feigned interest in your company and its products.
                        If it sounds like a candidate is asking questions for the sake of asking questions keep in mind they could be.
                        You can turn the tables by asking them how they plan to use the information they are gathering to be effective.
                    </p>
                </div>
            </div>
        </div>
    </div>


